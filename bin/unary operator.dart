void main(){
  int a =1;

  ///postfix operator  &  prefix operator
  /// postfix = [a++] , [a--]

  ///prefix
  print(++a); // a= a+1  1+1 =2
  print(--a); // a= a-1  2-1 =1

print("<<<<<<<<<<<<<<<<<<<<<<object>>>>>>>>>>>>>>>>>>>>>>");
  ///postfix
  print(a++); //a=1  a+1 = 1+1  2
  print('a= $a');
  print(a--); //a=1  a-1 = 2-1  1
  

}