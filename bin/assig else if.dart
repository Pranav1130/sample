import 'dart:async';

void main(){
  String size = 'XL';
  if (size == 'S'){
    print('S');
  }else if(size == 'M'){
    print('M');
  }else if(size == 'L'){
    print('L');
  }else if (size == 'XL'){
    print('XL');
  }else if (size == 'X'){
    print('X');
  }else{
    print('none of these');
  }
}